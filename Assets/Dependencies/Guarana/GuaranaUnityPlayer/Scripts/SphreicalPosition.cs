public class SphreicalPosition
{
    private float polar;
    private float azimuthal;
    private float radius;

    public float Polar
    {
        get => polar;
        set => polar = value;
    }

    public float Azimuthal
    {
        get => azimuthal;
        set => azimuthal = value;
    }

    public float Radius
    {
        get => radius;
        set => radius = value;
    }
}